CREATE TABLE municipis (
  id int NOT NULL AUTO_INCREMENT PRIMARY KEY,
  lot int NOT NULL,
  bloc int NOT NULL,
  comarca varchar(255) NOT NULL,
  codi_ine int NOT NULL,
  municipi varchar(255) NOT NULL,
