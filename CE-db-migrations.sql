  estancia_min time NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;

-- GEO Separat de la taula general per optimitzar consultes. A més, donat que com que creem index espacial, el valor no podrà ser NULL, cosa que no permetria inserir inicialment els valors a municipis, ja que no disposem de les coordenades encara.
CREATE TABLE municipis_geo (
  id int NOT NULL AUTO_INCREMENT PRIMARY KEY,
  municipi_id int NOT NULL,
  geopoint POINT SRID 4326 NOT NULL,
  SPATIAL INDEX idx_municipis_geo (geopoint),
  CONSTRAINT municipis_geo_fk_1 FOREIGN KEY (municipi_id) REFERENCES municipis (id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;

